package json;

public class Spell
{
    String name;
    String incantation;
    String effect;
    boolean canBeVerbal;
    String type;
    String light;

    public String getName()
    {
        return name;
    }

    public String getIncantation()
    {
        return incantation;
    }

    public String getEffect()
    {
        return effect;
    }

    public boolean getVerbal()
    {
        return canBeVerbal;
    }

    public String getType()
    {
        return type;
    }

    public String getLight()
    {
        return light;
    }
}
